#ARITHMETIC OPERATORS
a=10
b=5
print("addition:",a+b)
print("substraction:",a-b)
print("multiplication:",a*b)
print("division:",a/b)
print("floor_division:",a//b)
print("modulus:",a%b)
print("exponent:",a**b)


#Regional Operators
print("a>b is",a>b)
print("a<b is",a<b)
print("a=b is",a==b)
print("a!=b is",a!=b)
print("a>=b is",a!=b)
print("a>=b is",a>=b)


#Logical Operators
print("logical operator")
print(10<5 and 10<20)
print(10<5 or 10<20)
print(not(10<20))